#ifndef UNTITLED_TYPEALIAS_H
#define UNTITLED_TYPEALIAS_H


using u64 = unsigned long long int;
using u32 = unsigned long int;
using pui = uintptr_t;
using ui = unsigned int;
using u16 = unsigned short;
using u8 = unsigned char;

using i64 = long long int;
using i32 = long int;
using si = int;
using i16 = short;
using i8 = signed char;

using f32 = float;
using f64 = double;

#endif //UNTITLED_TYPEALIAS_H
